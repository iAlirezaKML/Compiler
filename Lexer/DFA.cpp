int DFA::trace(const string& buffer, int& first_pos, int& cur_pos) {
	int cur_state = 0;
	int next_state = cur_state;
	
	char cin;
	int input;

	while (next_state != -1) {
		cin = buffer.at(cur_pos);
		input = mapInput(cin);
		cur_state = next_state;
		next_state = dfa_table[cur_state][input];
		cur_pos++;
		if (!isLegal(input))
			return -2;
	}
	
	cur_pos--;
	
	if (isAccept(cur_state))
		return cur_state;

	return -1;
}


